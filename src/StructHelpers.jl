@noinline function throw_no_matching_instance(f,T,s)
    msg = """
    Cannot instaniate enum `T` from `s`. Got:
    s = $(repr(s))
    T = $(T)
    allowed values for s = $(map(f, instances(T)))
    """
    throw(ArgumentError(msg))
end

function def_symbol_or_string_from_enum_body(s_from_enum_generic, T)::Expr
    default = :($s_from_enum_generic(obj))
    matcharms = [
        :(obj === $inst) => QuoteNode(s_from_enum_generic(inst)) for inst in instances(T)
                ]
    ifelsechain(matcharms, default)
end

function def_symbol_from_enum(T)::Expr
    body = def_symbol_or_string_from_enum_body(symbol_from_enum_generic, T)
    :(
      function $SH.symbol_from_enum(obj::$T)::$Symbol
          $body
      end
     )
end

function def_string_from_enum(T)::Expr
    body = def_symbol_or_string_from_enum_body(string_from_enum_generic, T)
    :(
      function $SH.string_from_enum(obj::$T)::$String
          $body
      end
     )
end

function def_enum_from_string_or_symbol_body(f,T)
    err = :($throw_no_matching_instance($f,$T,s))
    matcharms = [
        :(s === $(f(inst))) => inst for inst in instances(T)
    ]
    ifelsechain(matcharms, err)
end

const ENUM_BATTERIES_DEFAULTS = (
    string_conversion = false,
    symbol_conversion = false,
    selfconstructor   = BATTERIES_DEFAULTS.selfconstructor,
    hash              = false,
    typesalt          = BATTERIES_DEFAULTS.typesalt,
)

const ENUM_BATTERIES_DOCSTRINGS = (
    string_conversion = "Add `convert(MyEnum, ::String)`, `MyEnum(::String)`, `convert(String, ::MyEnum)` and `String(::MyEnum)`",
    symbol_conversion = "Add `convert(MyEnum, ::Symbol)`, `MyEnum(::Symbol)`, `convert(Symbol, ::MyEnum)` and `Symbol(::MyEnum)`",
    selfconstructor   = BATTERIES_DOCSTRINGS.selfconstructor,
    hash              = BATTERIES_DOCSTRINGS.hash,
    typesalt          = BATTERIES_DOCSTRINGS.typesalt,
)

if (keys(ENUM_BATTERIES_DEFAULTS) != keys(ENUM_BATTERIES_DOCSTRINGS))
    error("""
          keys(ENUM_BATTERIES_DEFAULTS) == key(ENUM_BATTERIES_DOCSTRINGS) must hold.
          Got:
          keys(ENUM_BATTERIES_DEFAULTS) = $(keys(ENUM_BATTERIES_DEFAULTS))
          keys(ENUM_BATTERIES_DOCSTRINGS) = $(keys(ENUM_BATTERIES_DOCSTRINGS))
    """)
end
@assert keys(ENUM_BATTERIES_DEFAULTS) == keys(ENUM_BATTERIES_DOCSTRINGS)

function doc_enum_batteries_options()
    lines = map(propertynames(ENUM_BATTERIES_DEFAULTS)) do key
        "* **$key** = $(ENUM_BATTERIES_DEFAULTS[key]):\n $(ENUM_BATTERIES_DOCSTRINGS[key])"
    end
    join(lines, "\n")
end

const ENUM_BATTERIES_ALLOWED_KW = keys(ENUM_BATTERIES_DEFAULTS)

# Same shape as `ENUM_BATTERIES_DEFAULTS`, but with every Bool flipped to
# `false` and `typesalt` left at `nothing`. Used as the base nametuple for
# the `@enumbattery` macro. Derived programmatically so it stays in sync
# if `ENUM_BATTERIES_DEFAULTS` ever gains a new option.
const ENUM_BATTERIES_NONE = (;
    (k => (v isa Bool ? false : v) for (k, v) in pairs(ENUM_BATTERIES_DEFAULTS))...
)
@assert keys(ENUM_BATTERIES_NONE) == keys(ENUM_BATTERIES_DEFAULTS)

"""

    @enumbatteries T [options]

Automatically derive several methods for Enum type `T`.

# Example
```julia
@enum Color Red Blue Yellow
@enumbatteries Color
@enumbatteries Color hash=false # don't overload `Base.hash`
@enumbatteries Color symbol_conversion=true # allow convert(Color, :Blue), Color(:Blue), convert(Symbol, Blue), Symbol(Blue)
@enumbatteries Color symbol_conversion      # bare symbol is shorthand for `symbol_conversion=true`
```

Like [`@batteries`](@ref), arguments may also be `NamedTuple`-valued
expressions whose fields are spliced in (config-style):

```julia
const enum_config = (string_conversion=true, symbol_conversion=true)
@enumbatteries Color enum_config
@enumbatteries Color enum_config hash=false  # override
```

Supported options and defaults are:

$(doc_enum_batteries_options())

See also [`@enumbattery`](@ref) for an opt-in variant that derives only
the listed batteries instead of starting from the defaults.
"""
macro enumbatteries(T, kw...)
    def_enumbatteries(__module__, T, kw, ENUM_BATTERIES_DEFAULTS) |> esc
end

"""

    @enumbattery T [options]

Like [`@enumbatteries`](@ref) but with every default flipped to `false`:
only the batteries the user lists explicitly are derived. The
`enum_from_string` / `enum_from_symbol` / `string_from_enum` /
`symbol_from_enum` methods are always defined (they have no opt-out in
`@enumbatteries` either).

# Example
```julia
@enum Color Red Blue Yellow
@enumbattery Color symbol_conversion          # only the symbol-conversion batteries
@enumbattery Color hash typesalt=0xab         # only stable hash
```
"""
macro enumbattery(T, kw...)
    def_enumbatteries(__module__, T, kw, ENUM_BATTERIES_NONE) |> esc
end

# Shared codegen for `@enumbatteries` / `@enumbattery`. The two macros
# differ only in `base`: `ENUM_BATTERIES_DEFAULTS` (most batteries on) vs
# `ENUM_BATTERIES_NONE` (everything off, except the always-on
# `enum_from_*` / `*_from_enum` methods).
function def_enumbatteries(__module__, T, kw, base)
    nt = parse_all_macro_kw(kw, __module__, propertynames(ENUM_BATTERIES_DEFAULTS))
    for (pname, val) in pairs(nt)
        if !(pname in propertynames(ENUM_BATTERIES_DEFAULTS))
            error("""
                Unsupported keyword.
                Offending Keyword: $pname
                allowed: $ENUM_BATTERIES_ALLOWED_KW
                Got: $nt
            """)
        end
        if pname == :typesalt
            check_typesalt(val)
        elseif val isa Bool
            # pass
        else
            error("""
                Bad keyword argument value:
                Got: $nt
                Offending Keyword: $pname
                Offending value  : $(repr(val))
            """)
        end
    end
    # `typesalt` only makes sense with `hash=true`. If the user passed
    # `typesalt` without `hash`, infer `hash=true` so the salt isn't
    # silently ignored. Explicit `hash=false typesalt=...` still wins.
    nt = merge(base, (; hash = haskey(nt, :typesalt) || base.hash), nt)
    TT = Base.eval(__module__, T)::Type
    ret = quote end

    push!(ret.args, def_enum_from_symbol(TT))
    push!(ret.args, def_enum_from_string(TT))
    push!(ret.args, def_symbol_from_enum(TT))
    push!(ret.args, def_string_from_enum(TT))
    if nt.string_conversion
        ex1 = :($(Base).convert(::$Type{$TT}, s::$AbstractString) = $SH.enum_from_string($TT, $String(s)))
        ex2 = :($T(s::$AbstractString) = $SH.enum_from_string($TT, $String(s)))
        ex3 = :($(Base).convert(::$Type{$String}, x::$T) = $SH.string_from_enum(x))
        ex4 = :($(Base).String(x::$T) = $SH.string_from_enum(x))
        push!(ret.args, ex1, ex2, ex3, ex4)
    end
    if nt.symbol_conversion
        ex1 = :($(Base).convert(::$Type{$T}, s::$Symbol) = $SH.enum_from_symbol($TT, $Symbol(s)))
        ex2 = :($T(s::$Symbol) = $SH.enum_from_symbol($TT, $Symbol(s)))
        ex3 = :($(Base).convert(::$Type{$Symbol}, x::$T) = $SH.symbol_from_enum(x))
        ex4 = :($(Base).Symbol(x::$T) = $SH.symbol_from_enum(x))
        push!(ret.args, ex1, ex2, ex3, ex4)
    end
    if nt.selfconstructor
        def = def_selfconstructor(T)
        push!(ret.args, def)
    end
    if nt.hash
        def = :(function $(Base).hash(o::$T, h::$UInt)
            h = ($start_hash)(o, h, $(nt.typesalt))
            $(Base).hash($Integer(o), h)
        end
        )
        push!(ret.args, def)
    end
    push!(ret.args, def_has_batteries(T))
    return ret
end

