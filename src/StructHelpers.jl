"""
    default_keywords(::Type)::NamedTuple

Return a `NamedTuple` of default values for the fields of a struct.
Overload this for your type to supply defaults that are picked up by
the keyword constructor and the keyword `show` generated by
[`@batteries`](@ref) (with `kwconstructor=true` / `kwshow=true`).

* The keyword constructor uses the values returned here as defaults
  for the corresponding keyword arguments. Fields not present in the
  returned `NamedTuple` remain required.
* `kwshow` skips fields whose value is `isequal` to the default
  returned here.

# Example
```julia
struct Point
    x::Float64
    y::Float64
end
@batteries Point kwconstructor=true kwshow=true
StructHelpers.default_keywords(::Type{Point}) = (x = 0.0, y = 0.0)

Point()                       # Point(0.0, 0.0)
Point(x = 1.0)                # Point(1.0, 0.0)
sprint(show, Point(1.0, 0.0)) # "Point(x = 1.0)"
```
"""
function default_keywords(::Type)
    NamedTuple()
end

"""
